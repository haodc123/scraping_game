function myParseContent($conn, $page) {
    $xpath = myDOMXPath($page);

    $el_title = $xpath->query('//div[@class="game-title"]//h1');
        $title = $el_title->item(0) ? $el_title->item(0)->textContent : '';
    $s_title = slugify($title);
    echo $title.'<br />';

    $el_link = $xpath->query('//div[@class="input-container input-location"]/a');
        $link = $el_link->item(0) ? $el_link->item(0)->getAttribute('href') : '';
        $link .= '?gd_sdk_referrer_url='.$page;
    echo $link.'<br />';

    $el_type = $xpath->query('//span[@class="meta-type"]');
        $type = $el_type->item(0) ? $el_type->item(0)->textContent : '';
    echo $type.'<br />';

    $el_mode = $xpath->query('//div[span/text() = "Mobile Mode"]/span');
        $mode = $el_mode->item(1) ? $el_mode->item(1)->textContent : '';
    $dimension = $mode == 'Portrait' ? 1 : 0;
    echo $dimension.'<br />';

    // $el_cat = $xpath->query('//div[span/text() = "Categories"]/span/a');
    //     $cat = $el_cat->item(0) ? $el_cat->item(0)->textContent : '';
    // echo $cat.'<br />';
    
    $el_author = $xpath->query('//div[span/text() = "Company"]/span');
        $author = $el_author->item(1) ? $el_author->item(1)->textContent : '';
    echo $author.'<br />';

    $el_desc = $xpath->query('//div[span/text() = "Description"]/span');
        $desc = $el_desc->item(1) ? trim(mysqli_real_escape_string($conn, $el_desc->item(1)->textContent)) : '';
    echo $desc.'<br />';

    $el_guide = $xpath->query('//div[span/text() = "Instruction"]/span');
        $guide = $el_guide->item(1) ? trim(mysqli_real_escape_string($conn, $el_guide->item(1)->textContent)) : '';
    echo $guide.'<br />';

    $el_thumb = $xpath->query('//div[span/text() = "Thumbnails & Icons"]/div/div/span/img');
        $thumb_link = $el_thumb->item(0) ? $el_thumb->item(0)->getAttribute("src") : '';
    echo $thumb_link.'<br />';
    $thumb = makeThumbName($title, $thumb_link);

    file_put_contents('../res/thumb_kiddy/distribution/'.$thumb, file_get_contents($thumb_link));

    $vote = rand(3,5);
    $vote_time = rand(10, 20);
    $play_time = rand(21, 1144);

    saveDB($conn, $title, $s_title, $dimension, $type, $vote, $vote_time, $play_time, 0, $link, 'https://gamedistribution.com', $author, 28, 0, 0, 0, 0, $desc, $guide, $thumb, 1);
}

